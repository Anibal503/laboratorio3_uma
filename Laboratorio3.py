class Auto:
    def __init__(auto, marca, modelo, asientos, color, tipo, combustible, precio, garantia, fecha_ingreso):
        auto.marca = marca
        auto.modelo = modelo
        auto.asientos = asientos
        auto.color = color
        auto.tipo = tipo
        auto.combustible = combustible
        auto.precio = precio
        auto.garantia = garantia
        auto.fecha_ingreso = fecha_ingreso

